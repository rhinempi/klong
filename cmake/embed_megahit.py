    globals_dict = {
        '__name__': '__main__',
        '__file__': os.path.join(runtime_bin, 'megahit'),
        '__package__': None,
        '__cached__': None,
    }
    sys.argv[0] = os.path.realpath(sys.argv[0])
    code = compile(launcher_source, globals_dict['__file__'], 'exec')
