    runtime_bin = os.path.join(runtime_root, 'bin')
    os.makedirs(runtime_bin, exist_ok=True)

    core_path = os.path.join(runtime_bin, 'megahit_core')
    marker_path = os.path.join(runtime_root, '.hash')
    if not os.path.exists(core_path) or not os.path.exists(marker_path) or open(marker_path).read().strip() != EMBEDDED_HASH:
        tmp_path = core_path + '.tmp'
        with open(tmp_path, 'wb') as out:
            out.write(_decode(CORE_B64))
        os.chmod(tmp_path, os.stat(tmp_path).st_mode | stat.S_IXUSR | stat.S_IXGRP | stat.S_IXOTH)
        os.replace(tmp_path, core_path)
        with open(marker_path, 'w') as out:
            out.write(EMBEDDED_HASH + '\n')

    _safe_symlink('megahit_core', os.path.join(runtime_bin, 'megahit_core_popcnt'))
    _safe_symlink('megahit_core', os.path.join(runtime_bin, 'megahit_core_no_hw_accel'))
    _link_test_data(runtime_root, real_exe_dir)
    return runtime_bin


